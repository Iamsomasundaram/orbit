function clampScore(value) {
  return Math.max(0, Math.min(5, Number(value.toFixed(2))));
}

function getRecommendationDistance(a, b) {
  return Math.abs(RECOMMENDATION_RANK[a] - RECOMMENDATION_RANK[b]);
}

